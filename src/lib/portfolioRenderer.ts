function imageFrameCss(designSystem: PortfolioDesignSystem) {
  if (designSystem.theme.imageFrame === "soft_shadow") return "box-shadow: 0 3mm 9mm rgba(0,0,0,0.15);";
  if (designSystem.theme.imageFrame === "hairline") return "border: 0.25mm solid color-mix(in srgb, var(--secondary) 24%, transparent);";
  if (designSystem.theme.imageFrame === "inset_panel") return "padding: 2mm; background: rgba(255,255,255,0.24); border: 0.25mm solid color-mix(in srgb, var(--accent) 22%, transparent);";
  return "";
}

function buildCaption(page: Extract<PortfolioPlanPage, { title: string; year?: string; medium?: string; dimensions?: string }>) {
  const year = "year" in page ? page.year : undefined;
  const medium = "medium" in page ? page.medium : undefined;
  const dimensions = "dimensions" in page ? page.dimensions : undefined;
  return [page.title, year, medium, dimensions].filter((item) => item && !/unknown|n\/a|tbd/i.test(item)).join(", ");
}

function runVisualGate(input: PortfolioRenderInput & { htmlPath: string; pdfPath: string | null }): PortfolioVisualGateResult {
  const autoFixableIssues: PortfolioIssueClassification[] = [];
  const blockingIssues: PortfolioIssueClassification[] = [];
  const warnings: PortfolioIssueClassification[] = [];
  const suggestedRepairs: string[] = [];
  const pageCount = normalizedPages(input.plan).length;
  const externalText = fs.readFileSync(input.htmlPath, "utf8");
  const pdfSizeBytes = input.pdfPath && fs.existsSync(input.pdfPath) ? fs.statSync(input.pdfPath).size : 0;
  const usedFallbackPdf = Boolean(input.pdfPath && isFallbackPdf(input.pdfPath));
  const constraints = input.plan.portfolioConstraints || { targetPages: 20, minimumPages: 16, maximumPages: input.plan.maxPages || 24 };
  const maxPages = constraints.maximumPages || input.plan.maxPages;
  const targetFileSizeMb = input.plan.targetFileSizeMb;
  const workImagePages = input.plan.pages.filter((page) => page.type === "work_full_page" || page.type === "single_work_full_page" || "images" in page);
  const strategies = input.plan.pages.map((page) => page.layoutStrategy || page.type);
  const aestheticDiagnostics = buildAestheticDiagnostics(input.plan, externalText, input.copiedImages);
  const layoutStrategyCounts = countBy(strategies);
  const themeCounts = countBy(input.plan.pages.map((page) => page.design?.themeName || input.plan.designSystem?.themeName || "quiet_white"));
  const repeatedLayoutRuns = repeatedRuns(strategies, 4);
  const smallImagePages = findSmallImagePages(input.plan, input.copiedImages);
  const captionIssues = findCaptionIssues(input.plan);
  const webPreviewLanguageHits = aestheticDiagnostics.forbiddenExternalWords.filter((word) => /preview|mock|draft|placeholder|test/.test(word));

  for (const issue of input.preflightIssues) {
    if (/references no formal image paths|no structured|no image-bearing/i.test(issue)) blockingIssues.push(classify("no_usable_images", issue, "blocking"));
    else autoFixableIssues.push(classify("image_preflight", issue, "auto_fixable"));
  }
  if (!input.pdfPath) autoFixableIssues.push(classify("pdf_not_rendered", "Portfolio PDF was not rendered; retry or HTML remains available for inspection.", "auto_fixable"));
  if (usedFallbackPdf) blockingIssues.push(classify("fallback_pdf_not_professional", "Fallback PDF renderer was used; package is not professionally ready unless Playwright produces the final PDF.", "blocking"));
  if (forbiddenExternalPattern.test(externalText)) autoFixableIssues.push(classify("forbidden_terms", "Portfolio HTML contains forbidden external-facing terms.", "auto_fixable"));
  if (aestheticDiagnostics.forbiddenExternalWords.length) autoFixableIssues.push(classify("forbidden_terms", `Portfolio contains inappropriate external-facing words: ${aestheticDiagnostics.forbiddenExternalWords.join(", ")}.`, "auto_fixable"));
  if (aestheticDiagnostics.readsAsHtmlPreview) autoFixableIssues.push(classify("reads_as_html_preview", "Portfolio output reads as an HTML/browser preview instead of a formal PDF.", "auto_fixable"));
  if (genericStatementPattern.test(externalText)) autoFixableIssues.push(classify("statement_too_generic", "Portfolio statement contains generic application-language phrasing.", "auto_fixable"));
  if (pageCount < constraints.minimumPages) autoFixableIssues.push(classify("page_count_too_low", `Portfolio has ${pageCount} pages, below minimum ${constraints.minimumPages}.`, "auto_fixable"));
  if (maxPages && pageCount > maxPages) autoFixableIssues.push(classify("page_count_too_high", `Portfolio has ${pageCount} pages, exceeding maximum ${maxPages}.`, "auto_fixable"));
  if (targetFileSizeMb && pdfSizeBytes > targetFileSizeMb * 1024 * 1024) autoFixableIssues.push(classify("pdf_too_large", `Portfolio PDF is ${(pdfSizeBytes / 1024 / 1024).toFixed(2)} MB, exceeding target ${targetFileSizeMb} MB.`, "auto_fixable"));
  if (input.webResearchReferences.length < 3) warnings.push(classify("limited_references", "Fewer than three portfolio design/application references were recorded.", "warning"));
  if (workImagePages.length === 0) blockingIssues.push(classify("no_usable_images", "Portfolio plan has no image-bearing work pages.", "blocking"));
  if (!input.plan.layoutResearchUsed || input.plan.layoutResearchUsed.derivedPrinciples.length < 5) autoFixableIssues.push(classify("portfolio_research_missing", "Portfolio layout research is missing or too thin.", "auto_fixable"));
  if (!input.plan.layoutResearchUsed || input.plan.layoutResearchUsed.appliedPrinciples.length === 0) autoFixableIssues.push(classify("portfolio_research_not_applied", "Portfolio layout research was recorded but not applied to PortfolioPlan.", "auto_fixable"));
  if (!input.plan.curatorialSummary?.passedDiversityGate) {
    const availableSingleSeries = input.plan.curatorialSummary?.projectGroupCount <= 1 && input.plan.portfolioTitle !== "Selected Works";
    if (!availableSingleSeries) autoFixableIssues.push(classify("curatorial_gate_failed", "Portfolio did not pass project diversity, page rhythm, or layout strategy checks.", "auto_fixable"));
  }

  for (const copied of input.copiedImages) {
    if (copied.tooSmallForFullPage) autoFixableIssues.push({ ...classify("small_full_page_image", `Image may be too small for full-page use: ${copied.sourcePath} (${copied.width}x${copied.height}).`, "auto_fixable"), sourcePath: copied.sourcePath });
  }
  const plannedImageUses = input.plan.pages.flatMap((page, index) => {
    if (page.type === "work_full_page" || page.type === "single_work_full_page") return [{ path: page.imagePath, page: index + 1, layout: "single_work_full_page" }];
    if ("images" in page) return page.images.map((image) => ({ path: image.path, page: index + 1, layout: page.layoutStrategy || page.type }));
    return [];
  }).filter((use) => use.path);
  const plannedImagePaths = plannedImageUses.map((use) => use.path);
  const duplicateImages = excessiveDuplicateImagePaths(plannedImageUses);
  const copiedSourcePaths = new Set(input.copiedImages.map((image) => image.sourcePath));
  const missingReferencedImages = plannedImagePaths.filter((imagePath) => !copiedSourcePaths.has(imagePath));
  if (missingReferencedImages.length) blockingIssues.push(classify("missing_referenced_image", `Portfolio references ${missingReferencedImages.length} image file(s) that were not copied/readable.`, "blocking"));
  if (duplicateImages.length) autoFixableIssues.push(classify("duplicate_images", `Portfolio overuses ${new Set(duplicateImages).size} image file(s) beyond overview-to-detail rhythm.`, "auto_fixable"));
  for (const page of input.plan.pages) {
    if ("caption" in page && page.caption && page.caption.split(/\s+/).filter(Boolean).length > 35) autoFixableIssues.push(classify("caption_too_long", `Caption is longer than 35 words on page ${input.plan.pages.indexOf(page) + 1}.`, "auto_fixable"));
    if ((page.type === "series_grid" || page.type === "series_grid_large" || page.type === "series_overview_grid" || page.type === "image_research_grid") && page.images.length > 4) autoFixableIssues.push(classify("series_grid_too_dense", `Series grid has ${page.images.length} images and should be split into larger pages.`, "auto_fixable"));
  }
  if (new Set(strategies).size < Math.min(5, Math.max(3, input.plan.pages.length - 2))) autoFixableIssues.push(classify("layout_strategy_diversity_too_low", "Portfolio uses fewer than five layout strategies when material allows.", "auto_fixable"));
  if (aestheticDiagnostics.longestLayoutRun > 3) autoFixableIssues.push(classify("too_many_consecutive_layout_strategy", "More than three consecutive pages use the same layout strategy.", "auto_fixable"));
  if (aestheticDiagnostics.whiteOnlyPageCount > Math.ceil(pageCount * 0.7) && input.plan.designSystem?.themeName === "quiet_white") autoFixableIssues.push(classify("white_only_monotony", "Too many pages use a raw white/near-white page system without section rhythm.", "auto_fixable"));
  if (smallImagePages.length > 0) autoFixableIssues.push(classify("images_too_small_relative_to_page", `${smallImagePages.length} image page(s) appear too small relative to the page.`, "auto_fixable"));
  if (captionIssues.length > 0) autoFixableIssues.push(classify("captions_too_dense", `${captionIssues.length} page(s) have dense captions/text blocks.`, "auto_fixable"));
  if (/test preview|web preview|browser preview|mock|draft|placeholder/i.test(JSON.stringify(input.plan.pages.slice(0, 2)))) autoFixableIssues.push(classify("cover_looks_like_dev_page", "Cover or opening page contains development/preview language.", "auto_fixable"));

  const domCheck = tryCheckHtmlLayout(input.htmlPath);
  const pageScreenshots = safeOutputBaseName(input.outputBaseName || "portfolio") === "portfolio"
    ? capturePortfolioPageScreenshots(input.htmlPath, input.internalDir, "portfolio")
    : [];
  for (const issue of domCheck.issues) autoFixableIssues.push(classify(issue.includes("caption") ? "caption_too_small" : "layout_balance", issue, "auto_fixable"));
  const blankOrSparsePages = domCheck.issues.flatMap((issue) => {
    const match = issue.match(/Page\s+(\d+).*?(?:blank|short|sparse|small)/i);
    return match ? [Number(match[1])] : [];
  });
  const whiteOnlyPageRatio = pageCount ? aestheticDiagnostics.whiteOnlyPageCount / pageCount : 0;
  const aestheticScore = scoreAesthetic({
    pageCount,
    layoutStrategyCount: aestheticDiagnostics.layoutStrategyCount,
    longestLayoutRun: aestheticDiagnostics.longestLayoutRun,
    whiteOnlyPageRatio,
    blankOrSparsePages,
    smallImagePages,
    captionIssues,
    webPreviewLanguageHits
  });
  const professionalPdfScore = scoreProfessionalPdf({
    pdfPath: input.pdfPath,
    pdfSizeBytes,
    usedFallbackPdf,
    pageCount,
    constraints,
    missingImages: missingReferencedImages,
    webPreviewLanguageHits,
    aestheticScore
  });
  if (aestheticScore < 85) autoFixableIssues.push(classify("aesthetic_score_too_low", `Portfolio aesthetic score is ${aestheticScore}, below required 85.`, "auto_fixable"));
  if (professionalPdfScore < 85) autoFixableIssues.push(classify("professional_pdf_score_too_low", `Portfolio professional PDF score is ${professionalPdfScore}, below required 85.`, "auto_fixable"));
  suggestedRepairs.push(...autoFixableIssues.map((issue) => repairSuggestion(issue.code)));

  return {
    checkedAt: new Date().toISOString(),
    method: "HTML rendered with Playwright when available; PDF existence, page count, file size, text, image asset, and DOM layout checks.",
    opportunityTitle: input.opportunity.title,
    pageCount,
    targetPages: constraints.targetPages,
    minimumPages: constraints.minimumPages,
    maximumPages: constraints.maximumPages,
    pdfPath: input.pdfPath,
    pdfSizeBytes,
    pageScreenshots,
    layoutStrategyCounts,
    themeCounts,
    repeatedLayoutRuns,
    blankOrSparsePages,
    whiteOnlyPageRatio,
    webPreviewLanguageHits,
    missingImages: missingReferencedImages,
    smallImagePages,
    captionIssues,
    aestheticScore,
    professionalPdfScore,
    usedFallbackPdf,
    autoFixableIssues,
    blockingIssues,
    warnings,
    suggestedRepairs,
    copiedImages: input.copiedImages,
    aestheticDiagnostics,
    domCheck,
    passed: blockingIssues.length === 0 && autoFixableIssues.length === 0
  };
}

function buildAestheticDiagnostics(plan: PortfolioPlan, html: string, copiedImages: CopiedPortfolioImage[]): PortfolioVisualGateResult["aestheticDiagnostics"] {
  const themeName = plan.designSystem?.themeName || "quiet_white";
  const whiteOnlyPageCount = themeName === "quiet_white"
    ? plan.pages.filter((page) => !page.design || page.design.backgroundMode === "plain").length
    : 0;
  const strategies = plan.pages.map((page) => page.layoutStrategy || page.type);
  const longestLayoutRun = longestRun(strategies);
  const copiedBySource = new Map(copiedImages.map((image) => [image.sourcePath, image]));
  const pagesWithLikelySmallImages = plan.pages.filter((page) => {
    const images = page.type === "work_full_page" || page.type === "single_work_full_page"
      ? [copiedBySource.get(page.imagePath)]
      : "images" in page
        ? page.images.map((image) => copiedBySource.get(image.path))
        : [];
    return images.some((image) => image && (Math.max(image.width, image.height) < 1600 || Math.min(image.width, image.height) < 850));
  }).length;
  const pagesWithDenseCaptions = plan.pages.filter((page) => {
    const text = [
      "caption" in page ? page.caption : "",
      "text" in page ? page.text : "",
      page.type === "selected_works_list" ? page.works?.join(" ") : ""
    ].filter(Boolean).join(" ");
    return text.split(/\s+/).filter(Boolean).length > 60;
  }).length;
  const forbiddenExternalWords = [...new Set((html.match(/\b(?:TEST PREVIEW|Web preview|browser preview|mock|draft|placeholder|generated by AI|unknown|N\/A|TBD)\b/gi) || []).map((item) => item.toLowerCase()))];
  return {
    whiteOnlyPageCount,
    layoutStrategyCount: new Set(strategies).size,
    longestLayoutRun,
    pagesWithLikelySmallImages,
    pagesWithDenseCaptions,
    forbiddenExternalWords,
    readsAsHtmlPreview: /\bportfolio\.html\b|web preview|browser preview|test preview/i.test(html)
  };
}

function countBy(values: string[]) {
  return values.reduce<Record<string, number>>((counts, value) => {
    counts[value] = (counts[value] || 0) + 1;
    return counts;
  }, {});
}

function repeatedRuns(values: string[], minimumLength: number) {
  const runs: Array<{ strategy: string; startPage: number; length: number }> = [];
  let current = "";
  let start = 0;
  let length = 0;
  values.forEach((value, index) => {
    if (value === current) {
      length += 1;
    } else {
      if (current && length >= minimumLength) runs.push({ strategy: current, startPage: start + 1, length });
      current = value;
      start = index;
      length = 1;
    }
  });
  if (current && length >= minimumLength) runs.push({ strategy: current, startPage: start + 1, length });
  return runs;
}

function findSmallImagePages(plan: PortfolioPlan, copiedImages: CopiedPortfolioImage[]) {
  const copiedBySource = new Map(copiedImages.map((image) => [image.sourcePath, image]));
  return plan.pages.flatMap((page, index) => {
    const images = page.type === "work_full_page" || page.type === "single_work_full_page"
      ? [copiedBySource.get(page.imagePath)]
      : "images" in page
        ? page.images.map((image) => copiedBySource.get(image.path))
        : [];
    return images.some((image) => image && (Math.max(image.width, image.height) < 1600 || Math.min(image.width, image.height) < 850)) ? [index + 1] : [];
  });
}

function excessiveDuplicateImagePaths(uses: Array<{ path: string; page: number; layout: string }>) {
  const byPath = new Map<string, Array<{ page: number; layout: string }>>();
  for (const use of uses) byPath.set(use.path, [...(byPath.get(use.path) || []), { page: use.page, layout: use.layout }]);
  const excessive: string[] = [];
  for (const [imagePath, imageUses] of byPath) {
    const fullPageUses = imageUses.filter((use) => /single_work_full_page|work_full_page/.test(use.layout)).length;
    const overviewUses = imageUses.filter((use) => /overview|grid/.test(use.layout)).length;
    const nonOverviewUses = imageUses.length - overviewUses;
    if (imageUses.length > 2 || fullPageUses > 1 || nonOverviewUses > 1) excessive.push(imagePath);
  }
  return excessive;
}

function findCaptionIssues(plan: PortfolioPlan) {
  return plan.pages.flatMap((page, index) => {
    const issues: Array<{ page: number; issue: string }> = [];
    const caption = "caption" in page ? page.caption || "" : "";
    if (caption.split(/\s+/).filter(Boolean).length > 35) issues.push({ page: index + 1, issue: "caption longer than 35 words" });
    if (page.type === "short_statement" || page.type === "selected_works_list" || page.type === "contact" || page.type === "contact_page") return issues;
    const text = [
      caption,
      "text" in page ? page.text || "" : "",
    ].filter(Boolean).join(" ");
    if (text.split(/\s+/).filter(Boolean).length > 60) issues.push({ page: index + 1, issue: "page text/caption block is too dense" });
    return issues;
  });
}

function scoreAesthetic(input: {
  pageCount: number;
  layoutStrategyCount: number;
  longestLayoutRun: number;
  whiteOnlyPageRatio: number;
  blankOrSparsePages: number[];
  smallImagePages: number[];
  captionIssues: Array<{ page: number; issue: string }>;
  webPreviewLanguageHits: string[];
}) {
  let score = 100;
  if (input.pageCount >= 16 && input.layoutStrategyCount < 5) score -= 18;
  if (input.longestLayoutRun > 3) score -= (input.longestLayoutRun - 3) * 8;
  if (input.whiteOnlyPageRatio > 0.7) score -= 12;
  score -= Math.min(16, input.blankOrSparsePages.length * 4);
  score -= Math.min(12, input.smallImagePages.length * 3);
  score -= Math.min(12, input.captionIssues.length * 3);
  if (input.webPreviewLanguageHits.length) score -= 25;
  return Math.max(0, Math.min(100, Math.round(score)));
}

function scoreProfessionalPdf(input: {
  pdfPath: string | null;
  pdfSizeBytes: number;
  usedFallbackPdf: boolean;
  pageCount: number;
  constraints: { minimumPages: number; maximumPages: number };
  missingImages: string[];
  webPreviewLanguageHits: string[];
  aestheticScore: number;
}) {
  let score = input.aestheticScore;
  if (!input.pdfPath || input.pdfSizeBytes === 0) score -= 40;
  if (input.usedFallbackPdf) score -= 35;
  if (input.pageCount < input.constraints.minimumPages || input.pageCount > input.constraints.maximumPages) score -= 20;
  if (input.missingImages.length) score -= 30;
  if (input.webPreviewLanguageHits.length) score -= 25;
  return Math.max(0, Math.min(100, Math.round(score)));
}

function isFallbackPdf(pdfPath: string) {
  try {
    const header = fs.readFileSync(pdfPath, "utf8").slice(0, 120);
    return header.includes("ARTIST_STUDIO_FALLBACK_PDF");
  } catch {
    return false;
  }
}

function longestRun(values: string[]) {
  let longest = 0;
  let current = 0;
  let previous = "";
  for (const value of values) {
    current = value === previous ? current + 1 : 1;
    previous = value;
    longest = Math.max(longest, current);
  }
  return longest;
}

