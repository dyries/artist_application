import fs from "node:fs";
import { createRequire } from "node:module";
import path from "node:path";
import type { Opportunity, PortfolioIssueClassification, PortfolioPlan, PortfolioPlanImage, PortfolioPlanPage, PortfolioVisualGateResult } from "@/types/domain";

const requireFromRenderer = createRequire(import.meta.url);

export type CopiedPortfolioImage = {
  sourcePath: string;
  targetFileName: string;
  width: number;
  height: number;
  format: string;
  sizeBytes: number;
  optimized: boolean;
  tooSmallForFullPage: boolean;
};

type PortfolioRenderInput = {
  externalDir: string;
  internalDir: string;
  opportunity: Opportunity;
  plan: PortfolioPlan;
  copiedImages: CopiedPortfolioImage[];
  webResearchReferences: string[];
  preflightIssues: string[];
  outputBaseName?: string;
};

const forbiddenExternalPattern = /\b(mock|test pdf|review committee|draft|placeholder|generated by ai|for this open call|selected for|ready-to-copy|unknown|n\/a|tbd|dimensions recorded in source material)\b/i;
const genericStatementPattern = /\b(explores the relationship|acts as a space|creates dialogue|questions boundaries|liminal|poetic resonance|embodied experience)\b/i;

export function renderPortfolioPackage(input: PortfolioRenderInput) {
  const outputBaseName = safeOutputBaseName(input.outputBaseName || "portfolio");
  const imageBySource = new Map(input.copiedImages.map((image) => [image.sourcePath, image]));
  const html = renderPortfolioHtml(input.plan, imageBySource);
  const htmlFileName = outputBaseName === "portfolio" ? "portfolio.html" : `${outputBaseName}.html`;
  const pdfFileName = outputBaseName === "portfolio" ? "portfolio.pdf" : `${outputBaseName}.pdf`;
  const reportFileName = outputBaseName === "portfolio" ? "portfolio-visual-check.json" : `portfolio-visual-check-${outputBaseName}.json`;
  const htmlPath = path.join(input.externalDir, htmlFileName);
  fs.writeFileSync(htmlPath, html, "utf8");

  const pdfPath = renderHtmlToPdf(htmlPath, path.join(input.externalDir, pdfFileName));
  const visualReport = runVisualGate({ ...input, htmlPath, pdfPath });
  fs.writeFileSync(path.join(input.internalDir, reportFileName), JSON.stringify(visualReport, null, 2), "utf8");
  return { htmlPath, pdfPath, visualReport };
}

