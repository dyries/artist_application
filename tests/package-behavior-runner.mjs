function readPackage(folder) {
  const manifest = JSON.parse(fs.readFileSync(path.join(folder, "package-manifest.json"), "utf8"));
  const visual = JSON.parse(fs.readFileSync(path.join(folder, "internal-notes", "portfolio-visual-check.json"), "utf8"));
  const html = fs.readFileSync(path.join(folder, "external-submission", "portfolio.html"), "utf8");
  return { manifest, visual, html };
}

function longestStrategyRun(counts) {
  return counts.reduce((longest, run) => Math.max(longest, run.length), 0);
}

